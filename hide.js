function Menu() {
            var content = document.getElementById("content");
            if (content.style.display === "block") {
                content.style.height = "0";
                setTimeout(function() {
                    content.style.display = "none";
                }, 0.5);
            } else {
                content.style.display = "block";
                setTimeout(function() {
                    content.style.height = "auto";
                }, 0.5);
            }
        }
        
